
public class repetition {

    public static void main(String[] args) {
      
    //Repetition For
    System.out.println("for");
    for(int i=1;i<10;i++){
        System.out.println(i);
    }
    
    //Repetition While
    System.out.println("\nwhile");
    int a=1;
    while(a<10){
        System.out.println(a);
        a+=3;
    }
    //Repetition do-while
    System.out.println("\ndo while");
    int i =11;
    do{
        System.out.println(i);
        i++;
    }while(i<10);
  }
}
